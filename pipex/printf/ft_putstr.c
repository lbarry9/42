#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(2, "(null)", 6);
		(*len) += 6;
		return ;
	}
	while (s[i])
	{
		write(2, &s[i], 1);
		i++;
		(*len)++;
	}
}
